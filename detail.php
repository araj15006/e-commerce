<script>
    function addtocart(product_id, event) {
        // Prevent the default form submission behavior
        event.preventDefault();

        $.ajax({
            method: "GET",
            url: "add_to_cart.php",
            data: { id: product_id }
        })
        .done(function(data) {
            try {
                var msg = JSON.parse(data);
                console.log(msg); // Log the entire response for debugging
                
                if (msg.flag !== undefined) {

                    
                    if (msg.flag === true && msg.cart_total_qty !== undefined) {
                        $("#cart_qty").html(msg.cart_total_qty);
                    } else {
                        if (msg.is_login === 'no') {
                            alert("Sorry! You are not logged in");
                            

                            
                            // window.location.href = "user_login.php";
                        } else {
                            alert("Sorry! Data is not added to the cart");
                        }
                    }
                } else {
                    throw new Error("Invalid response format: 'flag' property is undefined");
                }
            } catch (error) {
                console.error(error);
                alert("An error occurred while processing the response. See the console for more details.");
            }
        })
        .fail(function(jqXHR, textStatus, errorThrown) {
            console.error("AJAX request failed:", textStatus, errorThrown);
            alert("AJAX request failed. See the console for more details.");
        });
    }
</script>






